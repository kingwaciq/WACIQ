# ================= GRADIENT =================
def tri(text):
    out = ""
    L = len(text)
    for i, ch in enumerate(text):
        p = i / max(L-1, 1)
        if p < 0.33:
            out += R + ch
        elif p < 0.66:
            out += Y + ch
        else:
            out += G + ch
    return out + RS

